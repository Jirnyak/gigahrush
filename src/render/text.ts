/** Draw text string onto a texture buffer at pixel position (ox, oy). */
function drawText(
  t: Uint32Array, text: string,
  ox: number, oy: number, fg: number,
): void {
  let cx = ox;
  for (const ch of text) {
    const g = G[ch];
    if (!g) { cx += CELL_W; continue; }
    for (let row = 0; row < GLYPH_H; row++) {
      const bits = g[row];
      for (let col = 0; col < GLYPH_W; col++) {
        if (bits & (0b10000 >> col)) {
          const px = cx + col;
          const py = oy + row;
          if (px >= 0 && px < S && py >= 0 && py < S) {
            t[py * S + px] = fg;
          }
        }
      }
    }
    cx += CELL_W;
  }
}

/** Measure pixel width of a text string. */
function measureText(text: string): number {
  return text.length > 0 ? text.length * CELL_W - 1 : 0;
}

/** Draw text horizontally centered on a texSize-wide texture at row oy. */
export function drawTextCentered(
  t: Uint32Array, text: string,
  oy: number, fg: number, texSize = S,
): void {
  const w = measureText(text);
  const ox = Math.floor((texSize - w) / 2);
  drawText(t, text, ox, oy, fg);
}
