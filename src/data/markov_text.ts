function atom(
  id: string,
  text: string,
  atomClass: MarkovAtomClass,
  tags: readonly string[],
  anchorKind: string,
  weight = 1,
  pcaDanger?: number,
  pcaWealth?: number,
  pcaNeed?: number,
): MarkovAtomDef {
  return { id, text, class: atomClass, tags, anchorKind, weight, pcaDanger, pcaWealth, pcaNeed };
}

function template(
  id: string,
  intent: MarkovIntent,
  domains: readonly string[],
  requiredTags: readonly string[],
  requiredAnchors: readonly string[],
  paths: readonly (readonly MarkovAtomClass[])[],
  fallback: string,
): MarkovTemplate {
  const expandedPaths = [
    ...paths,
    ...SKELETON_EXPANDED_PATHS.filter(p => paths.some(base => base[0] === p[0])),
  ];
  return {
    id,
    intent,
    source: 'generated_markov',
    domains,
    requiredTags,
    requiredAnchors,
    weight: 4 + requiredTags.length,
    maxChars: intent === 'bark_ambient' ? 96 : intent === 'demos_post' || intent === 'demos_reaction' ? 180 : 140,
    parts: [{ kind: 'slot', domain: domains[0] ?? 'space_move', minAtoms: 2, maxAtoms: 4, allowedClassPaths: expandedPaths, requiredAnchors }],
    fallback,
  };
}

function domain(
  id: string,
  tags: readonly string[],
  allowedIntents: readonly MarkovIntent[],
  atoms: readonly MarkovAtomDef[],
  corpusLines: readonly MarkovCorpusLine[],
  fallback: string,
): MarkovDomain {
  return {
    id,
    maxOrder: 3,
    tags,
    allowedIntents,
    atoms,
    corpus: corpusLines,
    fallback,
  };
}

function corpus(
  domainId: string,
  intent: MarkovIntent,
  prefix: string,
  lines: readonly string[],
  contextTags: readonly string[],
  anchorKinds: readonly string[],
  cap = 14,
): readonly MarkovCorpusLine[] {
  const out: MarkovCorpusLine[] = [];
  for (const line of lines) {
    if (out.length >= cap) break;
    if (!lineAllowedForCorpus(line)) continue;
    out.push({
      id: `${prefix}.${out.length}`,
      domain: domainId,
      intent,
      source: 'generated_markov',
      text: line,
      weight: 1,
      contextTags,
      anchorKinds,
    });
  }
  return out;
}

function corpusRecord(
  domainId: string,
  intent: MarkovIntent,
  prefix: string,
  record: Readonly<Record<number, readonly string[]>>,
  contextTags: readonly string[],
  anchorKinds: readonly string[],
  capPerKey = 5,
): readonly MarkovCorpusLine[] {
  const out: MarkovCorpusLine[] = [];
  for (const [key, lines] of Object.entries(record)) {
    out.push(...corpus(domainId, intent, `${prefix}.${key}`, lines, [...contextTags, `${prefix}.${key}`], anchorKinds, capPerKey));
  }
  return out;
}

function lineAllowedForCorpus(line: string): boolean {
  const lower = line.toLocaleLowerCase('ru-RU');
  const blacklist = [
    ...MARKOV_TONE_BLACKLIST,
    ...MARKOV_INTERNAL_BLACKLIST,
    ...MARKOV_SPOILER_BLACKLIST,
  ];
  return !blacklist.some(word => lower.includes(word));
}
