import os

extra_footer = """
#### Фаза 6: Оформление PR и Code Review
- **Название PR**: Должно строго соответствовать формату `feat(marx_X): краткое описание` или `fix(marx_X): краткое описание`.
- **Описание PR (Body)**: Укажите, какие именно изменения были внесены, какие слои были затронуты и какие тесты добавлены.
- **Самопроверка (Self-Review)**: Перед пушем проверьте свой диф (`git diff`), убедитесь, что нет закомментированного кода, консольных логов (`console.log`, кроме обоснованных предупреждений в `webgl.ts` или скриптах) и неиспользуемых импортов.

#### Фаза 7: Отчетность для Оркестратора
- По завершении работы вы должны не только создать PR, но и обновить статус задачи.
- Оставьте короткое, но емкое сообщение об успешном выполнении: "Задача marx_X выполнена. Все тесты пройдены. PR создан."
- Если в ходе работы вы обнаружили критический баг в смежной системе (например, в движке коллизий при работе над спрайтами), НЕ пытайтесь исправить его в рамках этого PR. Задокументируйте его и передайте информацию Оркестратору для создания новой задачи.
"""

for i in range(70, 80):
    filename = f"marx_{i}.md"
    if not os.path.exists(filename):
        continue
    
    with open(filename, 'r', encoding='utf-8') as f:
        content = f.read()
        
    if '#### Фаза 6: Оформление PR' not in content:
        content += "\n" + extra_footer
        with open(filename, 'w', encoding='utf-8') as f:
            f.write(content)
        
    print(f"Updated {filename} (new length: {len(content.splitlines())} lines)")

