import fs from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const CORPUS_DIR = path.join(ROOT, 'src', 'data', 'training_corpus');
const ARCHIVE_JSONL = path.resolve(ROOT, '..', 'gatbage', 'reference', 'scenario_writers', 'samosbor_source_texts.jsonl');

function ensureCorpusDir() {
  if (!fs.existsSync(CORPUS_DIR)) {
    fs.mkdirSync(CORPUS_DIR, { recursive: true });
  }
}

// 1. Извлечение Самосбор-текстов из samosbor_source_texts.jsonl
function exportSamosborCorpus() {
  const targetFile = path.join(CORPUS_DIR, 'samosbor.txt');
  if (!fs.existsSync(ARCHIVE_JSONL)) {
    console.log(`[Corpus Fetcher] JSONL архив не найден по адресу: ${ARCHIVE_JSONL}. Пропускаем Samosbor extraction.`);
    return;
  }

  const lines = fs.readFileSync(ARCHIVE_JSONL, 'utf8').split('\n').filter(Boolean);
  const chunks: string[] = [];

  for (const line of lines) {
    try {
      const record = JSON.parse(line);
      if (record.text && record.copy_policy !== 'blocked_for_game_text') {
        chunks.push(record.text.trim());
      }
    } catch {
      // Игнорируем некорректные строки
    }
  }

  if (chunks.length > 0) {
    fs.writeFileSync(targetFile, chunks.join('\n\n'), 'utf8');
    console.log(`[Corpus Fetcher] Экспортировано ${chunks.length} записей из архива в ${targetFile}`);
  }
}

