// 9. Создание/расширение корпуса лора Гигахруща
function exportGigahrushLoreCorpus() {
  const targetFile = path.join(CORPUS_DIR, 'gigahrush_lore.txt');
  const dataDir = path.join(ROOT, 'src', 'data');
  const filesToExtract = [
    'rumors.ts', 'tips.ts', 'markov_text.ts', 'notes.ts', 'contracts.ts',
    'dialogue.ts', 'context_lines.ts', 'items.ts', 'weapons.ts',
    'monster_ecology.ts', 'alife_population_plan.ts', 'samosbor_variants.ts'
  ];
  const extractedSentences: string[] = [];

  for (const file of filesToExtract) {
    const filePath = path.join(dataDir, file);
    if (!fs.existsSync(filePath)) continue;
    const content = fs.readFileSync(filePath, 'utf8');
    // Extract quotes containing Russian sentences ending in . ! or ?
    const matches = content.match(/["'`]([А-Яа-яЁё][^"'`\n\r]{10,250}[\.!\?])["'`]/g) || [];
    for (const match of matches) {
      const clean = match.slice(1, -1).replace(/\s+/g, ' ').trim();
      const wc = clean.split(' ').length;
      if (wc >= 3 && wc <= 45) {
        extractedSentences.push(clean);
      }
    }
  }

  if (extractedSentences.length > 0) {
    fs.writeFileSync(targetFile, extractedSentences.join('\n\n'), 'utf8');
    console.log(`[Corpus Fetcher] Сформирован обучающий корпус лора Гигахруща в ${targetFile} (${extractedSentences.length} предложений)`);
  }
}

