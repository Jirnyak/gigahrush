const fs = require('fs');

const suffix = `
### 9. Инструкция по написанию Unit-тестов (Обязательно)
Чтобы гарантировать стабильность архитектуры ГИГАХРУЩА, вы (агент Jules) обязаны:
1. Создать файл тестирования в директории \`tests/\` (если он еще не существует).
2. Написать моки для зависимых систем (например, мок глобального времени или мок генератора случайных чисел).
3. Покрыть тестами краевые случаи (edge cases):
   - Что произойдет, если сущность будет уничтожена прямо во время обновления?
   - Как система ведет себя на границах этажа (координаты < 0 или >= 1024)?
4. Запустить \`npm run test:unit\` и убедиться, что ваш код не сломал существующие тесты.

### 10. Требования к Pull Request (PR)
- Убедитесь, что ветка имеет префикс \`feat/\`, \`fix/\` или \`refactor/\`.
- Описание PR должно содержать перечень того, какие слои были затронуты (\`data/\`, \`systems/\` и т.д.).
- Если вы добавили новые данные в реестры (оружие, перки, НПЦ), приложите в описание PR скриншот или лог успешной валидации.
- Вы обязаны устранить все замечания линтера TypeScript (\`npm run typecheck\`) до отправки кода. Не игнорируйте ошибки типа \`any\`.

### 11. Заключительная валидация на мобильном устройстве
Так как ГИГАХРУЩ имеет браузерный таргет и ориентирован на мобильные устройства:
- Проверьте, не вызывает ли ваша фича утечек памяти (запустите \`npm run check:browser\`).
- Массивы, которые итерируются каждый кадр, должны использовать typed arrays (\`Int32Array\`, \`Float32Array\`), если в них хранятся только числа (ID, дистанции, флаги состояний).
- Убедитесь, что все новые UI-элементы поддерживают touch-события и корректно масштабируются на узких экранах.

**JULES, ПОДТВЕРДИТЕ ГОТОВНОСТЬ: ПРОЧТИТЕ ЭТОТ ДОКУМЕНТ ОТ НАЧАЛА ДО КОНЦА И НАЧИНАЙТЕ ВЫПОЛНЕНИЕ ПО ШАГАМ.**
`;

for (let i = 90; i <= 99; i++) {
  const file = `marx_${i}.md`;
  if (fs.existsSync(file)) {
    fs.appendFileSync(file, suffix);
  }
}
console.log("Appended suffix to marx_90.md - marx_99.md");
